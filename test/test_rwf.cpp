static void
test_mkdict( MDDict *dict ) noexcept
{
  MDOutput mout;
  MDMsgMem mem;
  MDMsg  * m;
  char     buf[ 8 * 1024 ];
  MDFid    fid_start, fid_end;
  uint32_t start, end;
  bool add_summary;
  uint8_t verb = DICT_VERB_NORMAL;

  add_summary = true;
  for ( fid_start = dict->min_fid; fid_start <= dict->max_fid; fid_start = fid_end ) {
    RwfSeriesWriter fld( mem, dict, buf, sizeof( buf ) );
    fld.encode_field_dictionary( fid_start, fid_end, verb, add_summary, 4 * 1024 )
       .end_msg();

    printf( "-- Dictionary test: %u -> %u\n", fid_start, fid_end );
    /*mout.print_hex( fld.buf, fld.off );*/
    m = MDMsg::unpack( fld.buf, 0, fld.off, RWF_SERIES_TYPE_ID, dict, mem );
    if ( m != NULL )
      m->print( &mout );
    mem.reuse();
    add_summary = false;
  }
  add_summary = true;
  for ( start = 1; start < dict->map_count; start = end ) {
    RwfSeriesWriter enu( mem, dict, buf, sizeof( buf ) );
    enu.encode_enum_dictionary( start, end, verb, add_summary, 4 * 1024 )
       .end_msg();

    printf( "-- Enum test: %u -> %u\n", start, end );
    /*mout.print_hex( enu.buf, enu.off );*/
    m = MDMsg::unpack( enu.buf, 0, enu.off, RWF_SERIES_TYPE_ID, dict, mem );
    if ( m != NULL )
      m->print( &mout );
    mem.reuse();
    add_summary = false;
  }
}


